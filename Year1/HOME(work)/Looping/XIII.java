package Looping;

public class XIII {
    public static void main(String[] args) {
        int i, f;
        for (i = 1; i <= 20; i++) {

            i *= 2;

            System.out.println(i);
        }
        for (f = 1; f <= 20; f++) {

            f *= 3;

            System.out.println(f);
        }
    }
}
